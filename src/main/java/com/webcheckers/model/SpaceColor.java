package com.webcheckers.model;

/**
 * This enum is a simple representation of the colors of the Space tiles
 * found on a checkers board.
 *
 * @author Dmitry Selin
 */
public enum SpaceColor {

    /** A representation of the white checkers board Spaces */
    WHITE,

    /** A representation of the black checkers board Spaces */
    BLACK
}
